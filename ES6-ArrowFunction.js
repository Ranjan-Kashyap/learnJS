// ARROW FUNCTIONS

//Regular Function:

function sayHello (){
    console.log('hello world!');
};

sayHello ();

//Arrow Function:

const sayHello2 = () => {
    console.log('hello world!');
}
sayHello2 ();

// If it's only one line you don't even need curly braces. You can write 
// above function like: 

// const sayHello = () => console.log('hello world');

// Also if you have one parameter for e.g (name) you don't need the parenthesis  you can write above function like 

// const sayHello = name => console.log(`hello ${name}`);



